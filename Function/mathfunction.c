#include <stdio.h>
#include <math.h>

int main()
{
    printf("-----// SELAMAT DATANG DI PROGRAM MATEMATIKA DASAR //-----\n");    
    printf("Hasil dari 3^4 = %g\n", pow(3, 4));
    printf("-----\n");
    printf("Hasil dari akar 16 = %g\n", sqrt(16));
    printf("-----\n");
    printf("Hasil dari pembulatan ke atas dari 2.5 = %g\n", ceil(2.5));
    printf("-----\n");
    printf("Hasil dari pembulatan ke bawah dari 2.5 = %g\n", floor(2.5));

    return 0;
}

